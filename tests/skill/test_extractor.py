class TestExampleBasedCreator:

    @pytest.fixture
    def llm(self):
        return StubLLMClient()

    @pytest.fixture
    def creator(self, llm):
        return ExampleBasedCreator(llm)

    @pytest.fixture
    def examples(self):
        return [
            {
                "session_excerpt": "The users table has no index on email column.",
                "finding": {"type": "missing_index", "table": "users", "column": "email"},
            },
            {
                "session_excerpt": "Query on orders.created_at takes 4.2 seconds.",
                "finding": {"type": "slow_query", "table": "orders", "column": "created_at"},
            },
            {
                "session_excerpt": "The products table is missing a foreign key to categories.",
                "finding": {"type": "missing_fk", "table": "products", "ref_table": "categories"},
            },
        ]

    def test_example_based_creator_infers_extraction_rules(self, creator, examples):
        """ExampleBasedCreator should produce valid YAML from examples."""
        result = creator.create(examples)
        parsed = yaml.safe_load(result)

        assert parsed is not None
        assert "name" in parsed
        assert "extraction" in parsed
        assert "entities" in parsed["extraction"]
        assert len(parsed["extraction"]["entities"]) > 0

        # Should have synthesis DAG
        assert "synthesis" in parsed
        node_ids = [n["id"] for n in parsed["synthesis"]["dag"]["nodes"]]
        assert "extract_entities" in node_ids

        # Should have session context
        assert parsed["session_context"]["mode"] == "skill_versioning"

        # Header should mention number of examples
        assert "3 examples" in result

    def test_example_based_creator_requires_at_least_3_examples(self, creator):
        """ExampleBasedCreator should raise ValueError with fewer than 3 examples."""
        with pytest.raises(ValueError, match="(?i)at least 3"):
            creator.create([
                {"session_excerpt": "test", "finding": {"type": "test"}},
                {"session_excerpt": "test2", "finding": {"type": "test2"}},
            ])

        # Zero examples should also fail
        with pytest.raises(ValueError, match="(?i)at least 3"):
            creator.create([])

        # One example should also fail
        with pytest.raises(ValueError, match="(?i)at least 3"):
            creator.create([{"session_excerpt": "x", "finding": {}}])
