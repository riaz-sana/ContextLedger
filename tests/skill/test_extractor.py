class TestPythonExtractor:

    @pytest.fixture
    def extractor(self):
        return PythonExtractor()

    @pytest.fixture
    def sample_file(self, tmp_path):
        f = tmp_path / "my_pipeline.py"
        f.write_text(SAMPLE_PYTHON, encoding="utf-8")
        return str(f)

    @pytest.fixture
    def empty_file(self, tmp_path):
        f = tmp_path / "empty_pipeline.py"
        f.write_text(SAMPLE_PYTHON_NO_CANDIDATES, encoding="utf-8")
        return str(f)

    def test_python_extractor_identifies_extraction_functions(self, extractor, sample_file):
        """PythonExtractor should find functions with candidate keywords."""
        result = extractor.extract(sample_file)
        parsed = yaml.safe_load(result)

        node_ids = [n["id"] for n in parsed["synthesis"]["dag"]["nodes"]]
        assert "extract_entities" in node_ids
        assert "analyse_results" in node_ids
        assert "synthesise_report" in node_ids
        assert "filter_low_confidence" in node_ids
        # helper_function should NOT be found
        assert "helper_function" not in node_ids

    def test_python_extractor_infers_dag_dependencies(self, extractor, sample_file):
        """PythonExtractor should detect calls between candidate functions."""
        result = extractor.extract(sample_file)
        parsed = yaml.safe_load(result)

        nodes_by_id = {n["id"]: n for n in parsed["synthesis"]["dag"]["nodes"]}

        # analyse_results calls extract_entities
        assert "extract_entities" in nodes_by_id["analyse_results"]["depends_on"]

        # synthesise_report calls analyse_results
        assert "analyse_results" in nodes_by_id["synthesise_report"]["depends_on"]

        # extract_entities calls nothing (in candidates)
        assert nodes_by_id["extract_entities"]["depends_on"] == []

    def test_python_extractor_generates_valid_profile_stub(self, extractor, sample_file):
        """PythonExtractor output should be valid YAML with required sections."""
        result = extractor.extract(sample_file)

        # Should contain header comments
        assert "Auto-generated by" in result
        assert "REVIEW" in result

        # Should be valid YAML
        parsed = yaml.safe_load(result)
        assert parsed is not None

        # Required top-level keys
        assert "name" in parsed
        assert "version" in parsed
        assert "extraction" in parsed
        assert "synthesis" in parsed
        assert "session_context" in parsed

        # Profile name derived from filename
        assert parsed["name"] == "my-pipeline-skill"
        assert parsed["version"] == "1.0.0"

        # Extraction section
        assert "entities" in parsed["extraction"]
        assert "sources" in parsed["extraction"]

        # Session context
        assert parsed["session_context"]["mode"] == "skill_versioning"
        assert parsed["session_context"]["cmv_enabled"] is True

    def test_python_extractor_handles_no_candidates_gracefully(self, extractor, empty_file):
        """When no candidate functions are found, still produce valid YAML."""
        result = extractor.extract(empty_file)
        parsed = yaml.safe_load(result)

        assert parsed is not None
        assert parsed["name"] == "empty-pipeline-skill"
        # DAG should have no nodes
        assert parsed["synthesis"]["dag"]["nodes"] == []
        # Entities should still have defaults
        assert len(parsed["extraction"]["entities"]) > 0

    def test_python_extractor_infers_node_types(self, extractor, sample_file):
        """PythonExtractor should map function name keywords to correct node types."""
        result = extractor.extract(sample_file)
        parsed = yaml.safe_load(result)

        nodes_by_id = {n["id"]: n for n in parsed["synthesis"]["dag"]["nodes"]}

        assert nodes_by_id["extract_entities"]["type"] == "extraction"
        assert nodes_by_id["analyse_results"]["type"] == "reasoning"
        assert nodes_by_id["synthesise_report"]["type"] == "synthesis"
        assert nodes_by_id["filter_low_confidence"]["type"] == "filter"


