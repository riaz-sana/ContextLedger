class TestClaudeSkillImporter:

    @pytest.fixture
    def llm(self):
        return StubLLMClient()

    @pytest.fixture
    def importer(self, llm):
        return ClaudeSkillImporter(llm)

    @pytest.fixture
    def skill_md_file(self, tmp_path):
        skill_dir = tmp_path / "frontend-design"
        skill_dir.mkdir()
        f = skill_dir / "SKILL.md"
        f.write_text(SAMPLE_SKILL_MD, encoding="utf-8")
        return str(f)

    def test_claude_skill_importer_wraps_skill_md(self, importer, skill_md_file):
        """ClaudeSkillImporter should reference the SKILL.md in refs."""
        result = importer.import_skill(skill_md_file)
        parsed = yaml.safe_load(result)

        assert parsed is not None
        # The SKILL.md path should be in refs
        assert skill_md_file in parsed["refs"]

        # Should have standard DAG structure
        node_ids = [n["id"] for n in parsed["synthesis"]["dag"]["nodes"]]
        assert "extract_entities" in node_ids
        assert "build_relationships" in node_ids
        assert "synthesise_findings" in node_ids

        # Profile name derived from parent directory
        assert parsed["name"] == "frontend-design-skill"

        # Should have session context
        assert parsed["session_context"]["mode"] == "skill_versioning"
        assert parsed["session_context"]["cmv_enabled"] is True

    def test_claude_skill_importer_infers_entities_from_content(self, importer, skill_md_file):
        """ClaudeSkillImporter should use LLM to infer entities from SKILL.md."""
        result = importer.import_skill(skill_md_file)
        parsed = yaml.safe_load(result)

        # The StubLLMClient returns entities when it sees "extract" and "entities"
        # in the prompt. Our prompt contains both keywords.
        assert "extraction" in parsed
        assert "entities" in parsed["extraction"]
        assert len(parsed["extraction"]["entities"]) > 0

        # Should also have sources
        assert "sources" in parsed["extraction"]

        # Header should mention the import source
        assert "Imported from" in result


