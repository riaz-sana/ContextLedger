SAMPLE_PYTHON = textwrap.dedent("""\
    \"\"\"Pipeline that extracts entities and synthesises reports.\"\"\"

    def extract_entities(raw_text: str) -> dict:
        \"\"\"Extract entity mentions from raw text.\"\"\"
        return {"entities": [{"type": "finding", "value": raw_text}]}

    def analyse_results(entities: list) -> dict:
        \"\"\"Analyse extracted entities for patterns.\"\"\"
        processed = extract_entities("test")
        return {"results": processed}

    def synthesise_report(analysis: dict) -> dict:
        \"\"\"Synthesise a report from analysis results.\"\"\"
        findings = analyse_results(analysis)
        return {"findings": findings}

    def helper_function(x):
        \"\"\"A helper that should NOT be picked up.\"\"\"
        return x + 1

    def filter_low_confidence(items: list) -> list:
        \"\"\"Filter out items below confidence threshold.\"\"\"
        return [i for i in items if i.get("confidence", 0) > 0.5]
""")

SAMPLE_PYTHON_NO_CANDIDATES = textwrap.dedent("""\
    \"\"\"A module with no extraction functions.\"\"\"

    def add(a, b):
        return a + b

    def multiply(a, b):
        return a * b
""")

SAMPLE_SKILL_MD = textwrap.dedent("""\
    # Frontend Design Skill

    This skill helps with frontend component design.
    It extracts component structures, design patterns, and accessibility findings
    from coding sessions.

    ## When to use
    - Reviewing React component architecture
    - Identifying accessibility issues
    - Documenting design decisions

    ## Entity types
    - Components
    - Design patterns
    - Accessibility findings
""")


